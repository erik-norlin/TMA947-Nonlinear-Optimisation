function dh = func2h(x)
r1=x(2).^2-0.5;
r2=x(1).^2+0.5;
r3=0.2*(1+r1./r2);

dh11a=0.8*cos(r3).*r1.^2.*x(1).^2./(r2.^4);
dh11b= 8*sin(r3).*r1.*x(1).^2./(r2.^3);
dh11c=-2*sin(r3).*r1./(r2.^2) + 0.036*x(1).^2;
dh11=dh11a+dh11b+dh11c;

dh12a=-0.8*x(1).*x(2).*r1.*cos(r3)./(r2.^3);
dh12b=-4*x(1).*x(2).*sin(r3)./(r2.^2);
dh12=dh12a+dh12b;

dh22=2*sin(r3)./r2+0.8*x(2).^2.*cos(r3)./(r1.*r2)+0.012*x(2).^2;
dh=[dh11,dh12; dh12,dh22];

